    /**
     * Assign a low priority call if any exist and  return it
     *
     * @param $operatorID
     *
     * @return Call
     */
    public function pick($operatorID)
    {
        $call = Call::getFirstLowPriorityCall();

        if($call)
        {
            $call->assign((int)$operatorID);
        }

        return $call;
